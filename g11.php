      <h1>Weapon Background</h1>
      <p>During the cold war era, caseless ammunition was one of NATO's top projects, to achieve both easier ammunition storage/logistics and faster rate of fire rifles. 
      In the end almost every project ended in failure or lost interest, with the exception of Heckler & Koch's G11. In theory caseless munition can bring about faster rate of weapon fire, 
      since the process of bolt unlocking followed by brass extraction and ejection is not needed with a bullet that doesn't come with a case, the weapon only needs to fire, load a new cartridge, repeat.</p>
      <p>This prototype assault rifle operates with a gas-operated rotary breech mechanism, fed with a 45-/50-round detachable magazine that is loaded in from the front of the weapon above the barrel, 
      primarily chambered for the 4.73×33mm caseless ammunition. The weapon is capable achieving a 460 rounds per minute fully automatic rate of fire, but G11's main selling point is the three-round burst fire 
      mode which is capable of reaching a cyclic rate of fire of 2100 rounds per minute, delivering three projectiles at a muzzle velocity of 930 metres per second. The burst rate of fire is so fast the recoil is felt after all three projectile leaves the weapon.</p>
      <p>Other features on the weapon includes the fire selector lever functioning as a safety as well as toggling between burst and automatic fires. An integrated optical sight that can be found on top of the carrying handle, that is removable to allow the use of other sights. 
      Thanks to the unconventional magazine placement, the weapon itself is relatively short, G11 measures 750mm in length while having a 540mm long barrel.</p>
      <p>The main cartridge designed for G11's usage is the 4.73×33mm caseless ammunition, it appears to be a cuboid shape as opposed to the conventional round bullet cartridges. It consists of 4 parts: 
      the solid propellant, the primer, the bullet, and a plastic cap that serves to keep the bullet centered in the propellant block. When primed the solid propellant fully ignites and the energy produced from burning forces the bullet out, 
      the plastic cap and the primer are ejected after being used. The small and rectangular square shape of the munition meant it stacked better, so more caseless cartridges can be fit in the same given volume than the conventional rounds. 
      However the solid propellant is brittle, which when handled improperly it may crack or shatter during the logistic process, cracked munitions have direct effects on the firing performance of G11.</p>
      <p>G11 project was handled by a joint research group made up of three different companies started in the 1960s. The finished weapon itself was a success but the munition development had a few major bottlenecking points 
      through out the twenty years of development, primarily plagued by the cook-off problem. The nitrocellulose initially used as the solid propellant was suffering from cook-off as continuous firing of the weapon heated up the chamber, 
      this was solved by using the HMX nitroamine high explosive as the new material since it had a much higher igniting temperature. Other minor issues such as weapon chamber fouling and logistic handling were also solved in time.</p>
      <p>The weapon was finished in 1990, although the project was technically successful, the weapon didn't enter full production due it's significantly high production cost for both the rifle and the munitions, the political changes 
      following Germany's reunification also cut down military funding for the newly reunited nation, as a result G11 was struck from procurement process. Only 265 units were ever produced, some of which made their way into the hands of the Bundeswehr. 
      Ultimately, the German armed forces replaced their G3 rifles with HK50/G36, which chambered for conventional ammunition. Even though the end of the G11 project didn't result in it's adoption, it's HITP round got the interest of AAI, wich use it as the base for the caseless variant of the LSAT program.</p>
