      <h1>Weapon Background</h1>
      <p>During a collaboration between the US Army Delta Force R&D department with the German fire arm company H&K, an European variant of the AR-15 platform of assault rifles was born. 
      The new carbine project was originally named HK M4, later on changed to HK416 after law suit filed by Colt Defense regarding trademark infringement. 
      Primarily chambered with the 5.56x45mm NATO cartridge, HK416 is a short-stroke gas piston operated, rotating bolt, selective fire carbine. Feeding on standard issue NATO STANAG 
      magazines HK416 is able achieve a rate of fire of 700-900 rounds per minute, default iron sight provides an effective firing range of 300 metres.</p>
      <p>416 features Picatinny rail placements all over the gun, top of the receiver, top/side/bottom of the barrel forearm. Compatible with and able to attach all forms of 
      combat accessories of NATO member states, the rail forearm does not come in contact with the barrel, the barrel is "free-floating" in effort to improve accuracy. 
      Bottom rails can mount accessories such as a fore-grip or a 40mm grenade launcher (usually the AG36/M320 style), while the side rails for tactical lights/laser and/or other combat accessories. 
      The weapon also features a folding front Iron sight paired with a rear sight similar to that of a G3, the operator can make use of it should they not wish to mount optics.</p>
      <p>The HK416 may be visually similar to that of a AR-15 rifle, it features several technology innovations of H&K, their HK50, XM8 and L85A2 projects has provided valuable experience in the development of HK416. 
      Different from the other AR-15 derivatives with the direct impingement mechanism, HK416 uses the proprietary short-stroke piston mechanism developed by H&K, which was derived from G36. Other minor features includes 
      newly designed adjustable telescopic butt stock and pistol grip to improve user comfort.</p>
      <p>Military/policing units all over the world are adapting the HK416 (Variants or derivatives) into their arsenal due to it's undeniable reliability, performance and modularity. Starting with the Norwegian army (HK416N), 
